Run instructions:

Simply build the project and run in Unity. The program will demonstrate various memory management techniques in C#, including garbage collection, 
    memory allocation, and deallocation.

Library is not present; it will be rebuilt automatically when you open the project in Unity. No additional setup is required.

How to replicate:
    - Clone the repository to your local machine using the command: git clone
    - Navigate to the project directory and add it to Unity Hub.
    - Run the project to see the memory management techniques in action.